function borbfun(...n){
    let zeroer = 0
    for(let checker of n){
        if(checker>0){
            zeroer = zeroer + checker
        }
    }
    return zeroer
}

let positivity = borbfun(2, -9, 5, 11, -30, 100, -8, -11, -4, 5, -6, 8)

console.log(positivity)

